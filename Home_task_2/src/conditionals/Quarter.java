package conditionals;

public class Quarter 
{
	/*Определить какой четверти принадлежит точка с координатами (х,у)*/
	public static int scnd_task(int x, int y)
	{
		int res = -1;
		if(x>0&y>0) 
			res=1;
		else if (x<0&y>0) 
			res=2;
		else if (x<0&y<0) 
			res=3;
		else if (x>0&y<0) 
			res=4;
		else 
			throw new IllegalArgumentException();
		return res;
	}
}
